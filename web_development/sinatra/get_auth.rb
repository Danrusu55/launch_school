require 'sinatra'

set(:auth) do |*roles|
  condition do
    unless logged_in? && roles.any? { |role| current_user.in_role? role }
      redirect "/login/", 303
    end
  end
end

get "/my/account/", :auth => [:user, :admin] do
  "Your Account Details"
end

get "/only/admin/", :auth => :admin do
  "Only admins are allowed here!"
end
